    <a name="top"></a>
    <font face="標楷體" size="4" color="#057D48">社團介紹 : </font><br>
    親愛的同學們，歡迎加入我們高雄大學山社這個大家庭！
    無論你是對大自然充滿好奇，還是希望透過戶外活動結交更多志同道合的朋友，
    這裡都能滿足你的一切需求！我們的山社不僅致力於<b>探索自然、保護環境</b>，
    還鼓勵每一位成員在大自然的懷抱中，<b>發現自己的潛力，挑戰自己的極限。</b>
    另外，我們的活動內容涵蓋了各種形式的戶外體驗，無論你是初次接觸登山、露營，
    還是已經擁有一定的戶外經驗，都能在這裡找到屬於你的樂趣。
    我們將帶你<b>穿越高雄的美麗山脈，探索不為人知的秘境</b>，和社團的朋友們一起<b>挑戰自我、突破極限</b>。
    每一項活動都將是一場<b>團隊合作</b>的考驗，也是一個發現自己、認識朋友的絕佳機會。<br>
