    <a name="third"></a>
    <font face="標楷體" size="4" color="#057D48">活動內容 : </font><br>
    <ul>
        <li><b>戶外團體挑戰</b>：不僅能加深同學間的友誼，還能鍛鍊身心，一起挑戰高雄的山林美景！
        <li><b>露營體驗</b>：晚上我們將帶領大家進入大自然，體驗最原始的露營樂趣，圍繞著營火談天說地，分享各自的故事。
        <li><b>登山健行</b>：一起登上高雄附近的美麗山脈，呼吸新鮮空氣，享受清新的大自然。
        <li><b>社團介紹與交流</b>：我們將介紹山野社的歷史與活動，並為大家準備了互動遊戲，讓新同學可以更快融入社團。
    </ul>
