    <font face="標楷體" size="4" color="#057D48">活動介紹 : </font><br>
    每學期，山野社都會精心籌辦一系列的迎新活動，讓每一位新同學都能夠快速融入社團，
    感受到山野社的溫暖與活力。今年，我們特別安排了豐富多彩的迎新活動，讓你在短短幾天內，
    <b>既能認識到我們社團的理念與精神，也能親身體驗到最具挑戰性與樂趣的戶外活動。</b>
    我們的迎新活動不僅是一場冒險，更是一個讓你結識新朋友、鍛鍊自我、放鬆心情、釋放壓力的完美契機！<br>
